function rmiKeys(filter) {
  return Object.keys(RMI_G).filter(k => {
    const g = RMI_G[k];
    const mv  = !filter.verbs  || filter.verbs.includes(g.verb);
    const mvo = !filter.voices || filter.voices.includes(g.voice);
    const mm  = !filter.moods  || filter.moods.includes(g.mood);
    const mt  = !filter.tenses || filter.tenses.includes(g.tense);
    return mv && mvo && mm && mt;
  });
}

function rmiGetStem(g) {
  const v = RMI_VERBS.find(x => x.verb === g.verb); if (!v) return g.verb;
  if (g.tense === "ενεστώτας")   return g.voice === "ενεργητική" ? v.stems.ae  : v.stems.me;
  if (g.tense === "παρατατικός") return g.voice === "ενεργητική" ? v.stems.apt : v.stems.mpt;
  if (g.tense === "αόριστος")    return g.voice === "ενεργητική" ? (v.stems.aaot||"") : (v.stems.maot||"");
  return v.verb;
}

function rmiBuildQText(g) {
  const v = `<em>${g.verb}</em>`;
  const tags = (...it) => '<div class="lq-tags">'+it.map(([c,t])=>`<span class="lq-tag ${c}">${t}</span>`).join('')+'</div>';
  const vO  = RMI_VERBS.find(x => x.verb === g.verb);
  const grp = vO ? (vO.group === "ομαλό" ? "Ομαλό" : "Ανώμαλο") : "";
  return `<div class="lq-main">Τύπος του ${v} στο <span class="lq-tag form" style="font-size:1rem;padding:1px 10px;vertical-align:middle;">${g.form}</span>:</div>`
       + tags(['voice', g.voice+' Φωνή'], ['tense', g.tense], ['mood', g.mood], ['tense', grp]);
}
