// ── GENERATOR ──────────────────────────────────────────────────────────────
// Χρησιμοποιεί _mc arrays για πλήρεις τύπους (MC mode)
// και en_act / pt_* / aor_* για καταλήξεις (FI mode)
RMI_VERBS.forEach(v => {
  const tenseMap = [
    { t:"ενεστώτας",
      actMc: v.en_act_mc, actFi: v.en_act,
      midMc: v.en_mid_mc, midFi: v.en_mid },
    { t:"παρατατικός",
      actMc: v.pt_act_mc, actFi: v.pt_act,
      midMc: v.pt_mid_mc, midFi: v.pt_mid },
  ];
  if (v.aor_act_mc) tenseMap.push({
    t:"αόριστος",
    actMc: v.aor_act_mc, actFi: v.aor_act,
    midMc: v.aor_mid_mc, midFi: v.aor_mid,
  });

  tenseMap.forEach(({ t, actMc, actFi, midMc, midFi }) => {
    if (actMc && actFi) {
      RMIP.forEach((p, i) => {
        const mc = [].concat(actMc[i]);
        const fi = [].concat(actFi[i]);
        rmiAdd(v.verb, p, "ενεργητική", "οριστική", t, mc, fi);
      });
    }
    if (midMc && midFi) {
      RMIP.forEach((p, i) => {
        const mc = [].concat(midMc[i]);
        const fi = [].concat(midFi[i]);
        rmiAdd(v.verb, p, "μέση", "οριστική", t, mc, fi);
      });
    }
  });
});

