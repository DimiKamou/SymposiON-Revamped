  // register one lesson as a GP_CONTENT dataset (theory-only, no loader)
  function _gpRegister(id, l) {
    try {
      if (window.GP_CONTENT && window.GP_CONTENT.registerDataset) {
        window.GP_CONTENT.registerDataset({
          id: id, icon: l.icon || '¶', label: (l.title || '') + (l.titleEm || ''),
          meta: 'Νεοελληνική Γλώσσα · Έκθεση · ' + (l.section || ''),
          tier: 'free', kind: 'neg', category: 'Έκθεση', source: 'neg', type: 'theory',
          loader: function () { return null; },
        });
      }
    } catch (_) {}
  }

  window.NEG_LESSONS = LESSONS;
  window.NEG = {
    get: function (id) { return LESSONS[id] || null; },
    ids: function () { return ORDER.slice(); },
    all: function () { return ORDER.map(function (id) { return LESSONS[id]; }); },
    has: function (id) { return !!LESSONS[id]; },
    // extension point: theory-neg-data2.js (and later θεματικές ενότητες,
    // παλιά θέματα) call this
    register: function (id, lesson) {
      if (!id || !lesson) return;
      lesson.id = id; LESSONS[id] = lesson;
      if (ORDER.indexOf(id) < 0) ORDER.push(id);
      _gpRegister(id, lesson);
    },
  };

  // register the built-in lessons (loads AFTER gp-content.js, so it exists)
  ORDER.forEach(function (id) { _gpRegister(id, LESSONS[id]); });
