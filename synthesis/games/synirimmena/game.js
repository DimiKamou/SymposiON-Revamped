const SYN_PREFIX = 'syn';
let _synFilter = null;

function _synSetMode(mode){
  gramSetMode(SYN_PREFIX,mode);
  document.querySelectorAll('#syn-wrap .lmode-btn').forEach(b=>b.classList.remove('selected'));
  document.getElementById(`syn-mode-${mode}`)?.classList.add('selected');
}

function openSynirimmena(levelId, mode){
  document.getElementById('syn-overlay').classList.add('active');
  document.body.style.overflow='hidden';
  if(!document.getElementById('syn-screen-levels')) _synBuild();
  // Auto-navigate to a specific level+mode when QR-scanned
  if (levelId) {
    setTimeout(() => {
      const card = document.querySelector(`#syn-level-grid .lvl-card[data-lvl-id="${levelId}"]`);
      if (card) card.click();
      if (mode) setTimeout(() => gramSetMode('syn', mode), 60);
    }, 60);
  }
}
function closeSynirimmena(){
  (_gramCleanup['syn']||function(){})();
  document.getElementById('syn-overlay').classList.remove('active');
  document.body.style.overflow='';
  const wrap=document.getElementById('syn-wrap');
  if(wrap){wrap.querySelectorAll('.lyo-screen').forEach(s=>s.classList.remove('active'));document.getElementById('syn-screen-levels')?.classList.add('active');}
}

// ── Build HTML ──
function _synBuild(){
  const wrap=document.getElementById('syn-wrap');
  wrap.innerHTML=gramBuildScreens(SYN_PREFIX,'Συνηρημένα Ρήματα','τιμῶ · ποιῶ · δηλῶ — Αρχαία Ελληνική Γραμματική');

  // Match screen is now generated by gramBuildScreens.
  // Override exit button so synirimmena's custom match uses _synMatchExit (not gramMatchExit).
  const matchExitBtn=document.querySelector('#syn-screen-match .lbtn-secondary');
  if(matchExitBtn) matchExitBtn.onclick=_synMatchExit;

  // Wire style fix for selects inside this overlay
  wrap.querySelectorAll('select').forEach(s=>{
    s.style.cssText='width:100%;padding:9px 12px;background:#241e16;border:1px solid #3d3020;border-radius:8px;color:#e8dcc8;font-size:0.93rem;cursor:pointer;font-family:inherit;';
  });

  // Build level grid
  gramBuildLevelGrid(SYN_PREFIX,SYN_LEVELS,lvl=>{
    _synFilter=lvl.filter;
    document.getElementById('syn-sett-title').textContent=`Επίπεδο ${lvl.id} — ${lvl.desc}`;
    document.getElementById('syn-sett-back').onclick=()=>{
      document.querySelectorAll('#syn-wrap .lyo-screen').forEach(s=>s.classList.remove('active'));
      document.getElementById('syn-screen-levels').classList.add('active');
    };
    document.querySelectorAll('#syn-wrap .lyo-screen').forEach(s=>s.classList.remove('active'));
    document.getElementById('syn-screen-settings').classList.add('active');
  });

  // Wire launch button
  document.getElementById('syn-launch-btn').onclick=_synLaunch;

  // Build polytonic keyboard
  gramBuildKeyboard(SYN_PREFIX);
}

// ── Launch ──
function _synLaunch(){
  const mode=gramGetMode(SYN_PREFIX);
  const timer=parseInt(document.getElementById('syn-sel-time').value);
  const lives=parseInt(document.getElementById('syn-sel-lives').value);

  if(mode==='match'){
    _synStartMatch(_synFilter);
    return;
  }

  gramRunGame({
    prefix:SYN_PREFIX,
    G:SYN_G,
    keysFn:synKeys,
    stemFn:synStem,
    qtFn:synQText,
    filter:_synFilter,
    mode,lives,timer,
    wrapId:'syn-wrap',
  });
}

