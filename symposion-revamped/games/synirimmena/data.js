function synAdd(v,p,vo,mo,te,c,u,acc,g){
  const k = g ? `${v}|${p}|${vo}|${mo}|${te}|${g}` : `${v}|${p}|${vo}|${mo}|${te}`;
  SYN_G[k]={verb:v,form:p,voice:vo,mood:mo,tense:te,gender:g||null,
             endings:acc||[c], fi_endings:acc||[c], uncontracted:u};
}

