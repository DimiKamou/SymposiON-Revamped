function afwKeys(filter){
  return Object.keys(AFW_G).filter(k=>{
    const g=AFW_G[k];
    const mv=!filter.verbs||filter.verbs.includes(g.verb);
    return mv&&filter.voices.includes(g.voice)&&filter.moods.includes(g.mood)&&filter.tenses.includes(g.tense)&&filter.forms.includes(g.form);
  });
}
