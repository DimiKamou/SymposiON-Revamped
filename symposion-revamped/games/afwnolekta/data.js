function afwAdd(verb,form,voice,mood,tense,mc,fi,gender){
  const k=gender?`${verb}|${form}|${voice}|${mood}|${tense}|${gender}`:`${verb}|${form}|${voice}|${mood}|${tense}`;
  AFW_G[k]={verb,form,voice,mood,tense,gender:gender||null,endings:mc,fi_endings:fi||mc};
}

