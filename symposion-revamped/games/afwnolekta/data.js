function afwBuildQText(g){
  const v=`<em>${g.verb}</em>`;
  const vO=AFW_VERBS.find(x=>x.verb===g.verb);
  const cl=vO?(vO.cat==="ουρανικό"?"Ουρανικό":vO.cat==="χειλικό"?"Χειλικό":"Οδοντικό"):"";
  const tags=(...it)=>'<div class="lq-tags">'+it.map(([c,t])=>`<span class="lq-tag ${c}">${t}</span>`).join('')+'</div>';
  if(g.form==="απαρέμφατο")return`<div class="lq-main">Κατάληξη <span class="lq-tag form" style="font-size:1rem;padding:1px 10px;">απαρεμφάτου</span> του ${v};</div>`+tags(['voice',g.voice+' Φωνή'],['tense',g.tense],['mood',cl]);
  if(g.form==="μετοχή")return`<div class="lq-main">Κατάληξη <span class="lq-tag form" style="font-size:1rem;padding:1px 10px;">μετοχής</span> του ${v};</div>`+tags(['voice',g.voice+' Φωνή'],['tense',g.tense],['gender',g.gender],['mood',cl]);
  return`<div class="lq-main">Κατάληξη του ${v} στο <span class="lq-tag form" style="font-size:1rem;padding:1px 10px;vertical-align:middle;">${g.form}</span>;</div>`+tags(['voice',g.voice+' Φωνή'],['tense',g.tense],['mood',g.mood],['tense',cl]);
}
